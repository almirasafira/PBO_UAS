    private void tabel() {
        dtm = new DefaultTableModel();
        dtm.addColumn("id");
        dtm.addColumn("NISN");
        dtm.addColumn("Nama");
        dtm.addColumn("Deskripsi");
        dtm.addColumn("Status");
        dtm.addColumn("Total");

        datamhs.setModel(dtm);
        loadPembayaran();
    }

    private void loadPembayaran() {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("ID");
        model.addColumn("Deskripsi");
        model.addColumn("Jumlah");
        model.addColumn("Status");

        try {
            String query = "SELECT id, deskripsiPembayaran, jumlahPembayaran, statusPembayaran FROM pembayaran WHERE NISN = ?";
            PreparedStatement pstmt = con.prepareStatement(query);
            pstmt.setString(1, mahasiswaNISN);
            ResultSet rs = pstmt.executeQuery();
            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getInt("id"),
                    rs.getString("deskripsiPembayaran"),
                    rs.getBigDecimal("jumlahPembayaran"),
                    rs.getString("statusPembayaran")
                });
            }
            datamhs.setModel(model);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal mengambil data pembayaran: " + e.getMessage());
        }
    }
    
    private void changeStatusPembayaran(String status) {
        int selectedRow = datamhs.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(null, "Pilih baris terlebih dahulu");
            return;
        }

        int idPembayaran = (int) dtm.getValueAt(selectedRow, 0); // Ambil ID Pembayaran dari kolom pertama

        String query = "UPDATE pembayaran SET statusPembayaran = ? WHERE id = ?";
        try (PreparedStatement preparedStatement = con.prepareStatement(query)) {
            preparedStatement.setString(1, status);
            preparedStatement.setInt(2, idPembayaran);

            int rowsAffected = preparedStatement.executeUpdate();
            if (rowsAffected > 0) {
                JOptionPane.showMessageDialog(null, "Status pembayaran diubah menjadi Lunas");
                tabel();
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "Error mengubah status pembayaran: " + ex.getMessage());
        }
    }
    
