    private void BayarPembayaran(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BayarPembayaran
        // TODO add your handling code here:
        int selectedRow = datamhs.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Pilih pembayaran yang ingin dibayar.");
            return;
        }

        int id = (int) datamhs.getValueAt(selectedRow, 0);
        try {
            String updateQuery = "UPDATE pembayaran SET statusPembayaran = 'Lunas' WHERE id = ?";
            PreparedStatement pstmt = con.prepareStatement(updateQuery);
            pstmt.setInt(1, id);
            pstmt.executeUpdate();

            JOptionPane.showMessageDialog(this, "Pembayaran berhasil dilakukan.");
            tabel(); // Refresh tabel untuk menampilkan status pembayaran yang diperbarui
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal memperbarui status pembayaran: " + e.getMessage());
        }
    }//GEN-LAST:event_BayarPembayaran

